#!/usr/bin/env python3
"""Compare ONNX model predictions against Mathematica reference predictions.

Usage:
    python scripts/validate_predictions.py

Requires:
    1. Mathematica reference .mat files in models/reference/
       (generated by: wolframscript -file scripts/export_to_onnx.wls)
    2. Model weights at ~/.kymobutler/models/

Generates comparison report and side-by-side visualizations.
"""

from pathlib import Path

import numpy as np

REPO_ROOT = Path(__file__).parent.parent
REF_DIR = REPO_ROOT / "models" / "reference"
TEST_DIR = REPO_ROOT / "tests" / "data"
OUT_DIR = REPO_ROOT / "tests" / "data" / "comparison"


def compare_arrays(name: str, onnx_pred: np.ndarray, mma_pred: np.ndarray) -> dict:
    """Compare two prediction arrays and print statistics."""
    # Resize onnx to match mma shape if needed
    if onnx_pred.shape != mma_pred.shape:
        from skimage.transform import resize
        print(f"  Resizing ONNX {onnx_pred.shape} -> Mathematica {mma_pred.shape}")
        onnx_pred = resize(
            onnx_pred, mma_pred.shape, anti_aliasing=True, preserve_range=True
        ).astype(np.float32)

    diff = np.abs(onnx_pred.astype(np.float64) - mma_pred.astype(np.float64))
    corr = np.corrcoef(onnx_pred.ravel(), mma_pred.ravel())[0, 1]

    stats = {
        "name": name,
        "onnx_shape": onnx_pred.shape,
        "mma_shape": mma_pred.shape,
        "onnx_range": (float(onnx_pred.min()), float(onnx_pred.max())),
        "mma_range": (float(mma_pred.min()), float(mma_pred.max())),
        "max_abs_diff": float(diff.max()),
        "mean_abs_diff": float(diff.mean()),
        "correlation": float(corr),
    }

    print(f"\n{'=' * 60}")
    print(f"  {name}")
    print(f"{'=' * 60}")
    print(f"  ONNX shape: {onnx_pred.shape}  range: [{onnx_pred.min():.4f}, {onnx_pred.max():.4f}]")
    print(f"  MMA  shape: {mma_pred.shape}  range: [{mma_pred.min():.4f}, {mma_pred.max():.4f}]")
    print(f"  Max abs diff:  {diff.max():.6f}")
    print(f"  Mean abs diff: {diff.mean():.6f}")
    print(f"  Correlation:   {corr:.6f}")

    if diff.max() < 1e-3:
        print("  -> MATCH (predictions are essentially identical)")
    elif diff.max() < 0.01:
        print("  -> CLOSE (minor numerical differences)")
    elif diff.max() < 0.1:
        print("  -> DIFFERENT (notable differences, may affect downstream)")
    else:
        print("  -> VERY DIFFERENT (likely a conversion issue)")

    return stats


def save_comparison_image(
    name: str, onnx_pred: np.ndarray, mma_pred: np.ndarray, out_dir: Path
) -> None:
    """Save side-by-side comparison as PNG."""
    from PIL import Image

    # Resize to match if needed
    if onnx_pred.shape != mma_pred.shape:
        from skimage.transform import resize
        onnx_pred = resize(
            onnx_pred, mma_pred.shape, anti_aliasing=True, preserve_range=True
        ).astype(np.float32)

    h, w = mma_pred.shape

    # Create side-by-side: ONNX | Mathematica | |Difference|*5
    diff = np.abs(onnx_pred.astype(np.float64) - mma_pred.astype(np.float64))
    diff_scaled = np.clip(diff * 5, 0, 1)  # 5x amplified difference

    canvas = np.zeros((h, w * 3 + 4), dtype=np.uint8)

    # ONNX prediction
    canvas[:, :w] = (np.clip(onnx_pred, 0, 1) * 255).astype(np.uint8)
    # Separator
    canvas[:, w:w+2] = 128
    # Mathematica prediction
    canvas[:, w+2:2*w+2] = (np.clip(mma_pred, 0, 1) * 255).astype(np.uint8)
    # Separator
    canvas[:, 2*w+2:2*w+4] = 128
    # Amplified difference
    canvas[:, 2*w+4:] = (diff_scaled * 255).astype(np.uint8)

    out_dir.mkdir(parents=True, exist_ok=True)
    Image.fromarray(canvas, mode="L").save(out_dir / f"{name}_comparison.png")
    print(f"  Saved: {out_dir / f'{name}_comparison.png'}")


def main():
    # Check for reference files
    if not REF_DIR.exists():
        print(f"Reference directory not found: {REF_DIR}")
        print("Run: wolframscript -file scripts/export_to_onnx.wls")
        return

    ref_files = list(REF_DIR.glob("*.mat"))
    if not ref_files:
        print(f"No .mat reference files in {REF_DIR}")
        print("Run: wolframscript -file scripts/export_to_onnx.wls")
        return

    print(f"Found {len(ref_files)} reference files: {[f.name for f in ref_files]}")

    from scipy.io import loadmat

    from kymobutler.models.weights import load_default_models
    from kymobutler.segmentation import segment_bidirectional, segment_unidirectional

    print("\nLoading ONNX models...")
    models = load_default_models()

    OUT_DIR.mkdir(parents=True, exist_ok=True)

    comparisons = []

    # Bidirectional: bitest.png
    bitest_ref = REF_DIR / "bitest_binet_pred.mat"
    if bitest_ref.exists():
        mma_pred = loadmat(bitest_ref)["pred"].astype(np.float32)
        _, _, _, onnx_pred = segment_bidirectional(
            TEST_DIR / "bitest.png", models["binet"]
        )
        stats = compare_arrays("bitest_binet", onnx_pred, mma_pred)
        save_comparison_image("bitest_binet", onnx_pred, mma_pred, OUT_DIR)
        comparisons.append(stats)

    # Unidirectional: unitest.png, unitest2.png
    for test_name in ("unitest", "unitest2"):
        for channel in ("ant", "ret"):
            ref_path = REF_DIR / f"{test_name}_{channel}_pred.mat"
            if ref_path.exists():
                mma_pred = loadmat(ref_path)["pred"].astype(np.float32)
                _, _, _, onnx_pred_dict = segment_unidirectional(
                    TEST_DIR / f"{test_name}.png", models["uninet"]
                )
                stats = compare_arrays(
                    f"{test_name}_{channel}", onnx_pred_dict[channel], mma_pred
                )
                save_comparison_image(
                    f"{test_name}_{channel}", onnx_pred_dict[channel], mma_pred, OUT_DIR
                )
                comparisons.append(stats)

    # Load reference track counts if available
    import json
    ref_counts_path = REF_DIR / "reference_track_counts.json"
    if ref_counts_path.exists():
        with open(ref_counts_path) as f:
            ref_counts = json.load(f)
        print(f"\n{'=' * 60}")
        print("  Mathematica reference track counts")
        print(f"{'=' * 60}")
        for key, count in ref_counts.items():
            print(f"  {key}: {count}")

    # Summary
    print(f"\n{'=' * 60}")
    print("  SUMMARY")
    print(f"{'=' * 60}")
    for c in comparisons:
        status = "OK" if c["max_abs_diff"] < 0.01 else "DIFFERS"
        print(f"  [{status}] {c['name']}: max_diff={c['max_abs_diff']:.6f}, corr={c['correlation']:.6f}")

    print(f"\nComparison images saved to: {OUT_DIR}")


if __name__ == "__main__":
    main()
